package me.lowscarlet.pratikum9_4;

public class DataModel {
    private String id;
    private String nama;
    private String alamat;
    private String no_penjual;
    private String kode_barang;
    private Integer jumlah_penjualan;
    private Integer harga_satuan;
    private Integer diskon;

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public String getNo_penjual() {
        return no_penjual;
    }

    public String getKode_barang() {
        return kode_barang;
    }

    public Integer getJumlah_penjualan() {
        return jumlah_penjualan;
    }

    public Integer getHarga_satuan() {
        return harga_satuan;
    }

    public Integer getDiskon() {
        return diskon;
    }
}
